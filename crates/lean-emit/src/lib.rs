use gadget_ir::{Constraint, Gadget, Term};

/// Emit a Lean 4 proof scaffold from a gadget definition.
///
/// The output contains imports, witness parameters, constraint hypotheses,
/// and a `sorry` proof. The theorem statement is machine-generated;
/// the proof is left for a human or LLM to fill in.
pub fn emit_lean(gadget: &Gadget) -> String {
    let names: Vec<&str> = gadget.witnesses.iter().map(|w| w.name.as_str()).collect();
    let theorem_name = gadget.name.replace('-', "_");

    let mut out = String::new();

    // -- imports
    out.push_str("import ZkGadgets.Field\n");
    out.push_str("import Mathlib.Data.ZMod.Basic\n");
    out.push_str("import Mathlib.Algebra.Field.ZMod\n\n");

    // -- doc comment
    out.push_str(&format!("/-!\n# {}\n\n", gadget.name));
    out.push_str("Auto-generated by lean-emit. Do not edit the theorem statement.\n\n");
    out.push_str("Constraints:\n");
    for c in &gadget.constraints {
        out.push_str(&format!("  {} : {} = 0\n", c.label, emit_constraint_expr(c, &names)));
    }
    if let Some(spec) = &gadget.soundness_spec {
        out.push_str(&format!("\nSoundness: {}\n", spec));
    }
    out.push_str("-/\n\n");

    // -- field variable
    out.push_str("variable (p : ℕ) [Fact (Nat.Prime p)]\n\n");

    // -- theorem
    out.push_str(&format!("theorem {}_sound\n", theorem_name));

    // witness parameters
    out.push_str(&format!("    ({} : ZMod p)\n", names.join(" ")));

    // constraint hypotheses
    for c in &gadget.constraints {
        let label = c.label.replace('-', "_");
        let expr = emit_constraint_expr(c, &names);
        out.push_str(&format!("    (h_{} : {} = 0)\n", label, expr));
    }

    // conclusion
    let conclusion = match &gadget.soundness_spec {
        Some(spec) => format!("    : {}", spec),
        None => "    : True".to_string(),
    };
    out.push_str(&conclusion);
    out.push_str(" := by\n  sorry\n");

    out
}

/// Render a constraint as a Lean expression (without `= 0`).
fn emit_constraint_expr(c: &Constraint, names: &[&str]) -> String {
    if c.terms.is_empty() {
        return "0".to_string();
    }

    let mut parts = Vec::new();
    for (i, term) in c.terms.iter().enumerate() {
        parts.push(emit_term(term, names, i == 0));
    }
    parts.join(" ")
}

/// Render a single term with sign handling.
///
/// `is_first` controls whether a leading `+` is suppressed.
fn emit_term(term: &Term, names: &[&str], is_first: bool) -> String {
    let coeff = term.coeff.trim();
    let negative = coeff.starts_with('-');
    let abs_coeff = if negative { &coeff[1..] } else { coeff };

    // monomial: product of witness names
    let monomial: Option<String> = if term.vars.is_empty() {
        None
    } else {
        let var_names: Vec<&str> = term.vars.iter().map(|&id| names[id]).collect();
        Some(var_names.join(" * "))
    };

    // absolute expression (no sign)
    let expr = match (abs_coeff, &monomial) {
        ("1", Some(m)) => m.clone(),
        (c, Some(m)) => format!("{} * {}", c, m),
        ("1", None) => "(1 : ZMod p)".to_string(),
        (c, None) => format!("({} : ZMod p)", c),
    };

    // attach sign
    if is_first {
        if negative {
            format!("-({})", expr)
        } else {
            expr
        }
    } else if negative {
        format!("- {}", expr)
    } else {
        format!("+ {}", expr)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    fn examples_dir() -> std::path::PathBuf {
        std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .unwrap()
            .parent()
            .unwrap()
            .join("examples")
    }

    #[test]
    fn emit_range_check() {
        let gadget =
            gadget_ir::load_gadget_file(&examples_dir().join("range-check/gadget.toml")).unwrap();
        let out = emit_lean(&gadget);

        // has theorem with correct name
        assert!(out.contains("theorem range_check_8bit_sound"));
        // has all witnesses
        assert!(out.contains("(x b0 b1 b2 b3 b4 b5 b6 b7 : ZMod p)"));
        // bit constraint renders correctly: b0 * b0 - b0 = 0
        assert!(out.contains("h_bit_0 : b0 * b0 - b0 = 0"));
        // decomposition has weighted terms
        assert!(out.contains("h_decomposition :"));
        assert!(out.contains("128 * b7"));
        assert!(out.contains("- x = 0"));
        // ends with sorry
        assert!(out.contains("sorry"));
        // soundness spec in conclusion
        assert!(out.contains("∃ k : Fin 256"));
    }

    #[test]
    fn emit_conditional_select() {
        let gadget = gadget_ir::load_gadget_file(
            &examples_dir().join("conditional-select/gadget.toml"),
        )
        .unwrap();
        let out = emit_lean(&gadget);

        assert!(out.contains("theorem conditional_select_sound"));
        assert!(out.contains("(b x y z : ZMod p)"));
        // boolean constraint: b * b - b = 0
        assert!(out.contains("h_boolean : b * b - b = 0"));
        // select constraint: b * x + y - b * y - z = 0
        assert!(out.contains("h_select : b * x + y - b * y - z = 0"));
        assert!(out.contains("sorry"));
    }
}
